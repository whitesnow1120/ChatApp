const mongoose = require("mongoose");
const Schema = mongoose.Schema;

// Create Schema for users
const userSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  username: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  createdAt: {
    type: String,
    default: Date.now,
  },
  updatedAt: {
    type: String,
    default: Date.now,
  },
});

const User = mongoose.model("users", userSchema);
module.exports = User;
